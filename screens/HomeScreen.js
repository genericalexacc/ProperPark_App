import { WebBrowser, Camera, Permissions } from 'expo';
import { Icon } from 'expo';
import { MonoText } from '../components/StyledText';
import Colors from '../constants/Colors';

export default class HomeScreen extends React.Component {
  
  state = {
    hasCameraPermission: null,
    type: Camera.Constants.Type.back,
  };

  async componentDidMount() {
    const { status } = await Permissions.askAsync(Permissions.CAMERA);
    this.setState({ hasCameraPermission: status === 'granted' });
  }


  static navigationOptions = {
    header: null,
  };

  snap = async () => {
    if (this.camera) {
      let photo = await this.camera.takePictureAsync({base64: true, quality: 0.05});
      fetch('http://localhost:3000/api/test', {
        method: 'GET',
        headers: {
          'Accept': 'application/json',
          'Content-Type': 'application/json',
        }
      }).then((data) => {
        console.log(data)
      })
      console.log(photo.base64)
    }
  };

  render() {
    const { hasCameraPermission } = this.state;
    if (hasCameraPermission === null) {
      return <View />;
    } else if (hasCameraPermission === false) {
      return <Text>No access to camera</Text>;
    } else {
    return (
      <View style={styles.container}>

      <View style={{ flex: 1 }}>
          <Camera style={{ flex: 1 }} type={this.state.type}  ref={ref => { this.camera = ref }}>
            <View 
              style={{
                flex: 1,
                alignSelf: 'center',
                backgroundColor: 'transparent',
                flexDirection: 'row',
              }}>
              <TouchableOpacity
                style={{
                  flex: 0.1,
                  alignSelf: 'flex-end',
                  alignItems: 'center',
                  backgroundColor: 'white', borderRadius:25, paddingLeft: 20, paddingRight: 20, paddingTop: 7, paddingBottom: 7, marginBottom: 25
                }}
                onPress={() => {
                  this.snap()
                }}>
              <Icon.Ionicons
                name={
                  Platform.OS === 'ios'
                    ? `ios-camera`
                    : 'md-camera'
                }
                size={45}
                color={Colors.tabIconSelected}
              />
              </TouchableOpacity>
            </View>
          </Camera>
        </View>
      </View>
    );
  }
}

  _maybeRenderDevelopmentModeWarning() {
    if (__DEV__) {
      const learnMoreButton = (
        <Text onPress={this._handleLearnMorePress} style={styles.helpLinkText}>
          Learn more
        </Text>
      );

      return (
        <Text style={styles.developmentModeText}>
          Development mode is enabled, your app will be slower but you can use useful development
          tools. {learnMoreButton}
        </Text>
      );
    } else {
      return (
        <Text style={styles.developmentModeText}>
          You are not in development mode, your app will run at full speed.
        </Text>
      );
    }
  }

  _handleLearnMorePress = () => {
    WebBrowser.openBrowserAsync('https://docs.expo.io/versions/latest/guides/development-mode');
  };

  _handleHelpPress = () => {
    WebBrowser.openBrowserAsync(
      'https://docs.expo.io/versions/latest/guides/up-and-running.html#can-t-see-your-changes'
    );
  };
}

